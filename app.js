/**
 * while (<condition>) {
 * This code repeats as long as the condition is met
 * }
 */
let i = 0; //initiator
while (i < 10) { //condition
    console.log("Hello World");
    i++; //i = i + 1;
}

/*
 *infinite cycle
 for (;;) 
 */
for (let i = 0; i < 10; i++) {
    console.log("Hello World");   
}